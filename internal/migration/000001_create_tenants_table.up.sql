CREATE TABLE tenants (
  id BIGSERIAL PRIMARY KEY,
  name VARCHAR(100) NOT NULL,
  type VARCHAR(10) NOT NULL DEFAULT 'NORMAL',
  description TEXT,
  key VARCHAR(255) NOT NULL,
  secret VARCHAR(255) NOT NULL,
  callback_url VARCHAR(255),
  push_total BIGINT NOT NULL DEFAULT 0,
  push_failed BIGINT NOT NULL DEFAULT 0,
  user_id BIGINT,
  status SMALLINT NOT NULL DEFAULT 1,
  activate SMALLINT NOT NULL DEFAULT 1,
  deleted_date TIMESTAMP NULL DEFAULT NULL,
  created_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  row_created_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  row_updated_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  UNIQUE (name),
  UNIQUE (key),
  UNIQUE (secret)
);